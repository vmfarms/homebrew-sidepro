    end
  
    on_linux do
      on_intel do
        url "https://shared-clients-api.minio.beta.stack.io/sidepro-cli/channels/beta/sidepro-linux-x64.tar.gz"
        sha256 "0791c3e55a6653bfdb701d49be420bb8664f4171e893f402dfd33d7805afe8ca"
