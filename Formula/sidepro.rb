    on_macos do
      on_intel do
        url "https://shared-clients-api.minio.beta.stack.io/sidepro-cli/channels/beta/sidepro-darwin-x64.tar.gz"
        sha256 "3c24a815679fc6cd5d62b08ddbedb7f36eadc99bb5ffb33b642c77ecd439351c"
